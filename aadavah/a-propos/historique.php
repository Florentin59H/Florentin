			<h1>Historique de l’A.A.D.V.A.H du Nord</h1>
			<p>C’est en 1928 qu’était lancé à Seclin un cri de ralliement par un grand mutilé du travail qui avait pour nom Albert SCHRYVER né le 14 mars 1896, victime d’un grave accident du travail en 1908 à l’âge de 12 ans dans une usine textile de Seclin, amputé du bras droit, qui fut l’un des premiers militants du groupement du Nord des mutilés du travail et fondateur de la section de Seclin.</p>
			<p>Son appel fut entendu et c’est en 1930 qui devait être constituée la section de Seclin. Il est décédé le 22 juillet 1968.</p>
			<p>Il avait prévu la continuité de la marche de la section en faisant désigner deux jeunes et dynamiques responsables de la section qui ont élargi le périmètre regroupant les communes environnantes en ouvrant de nouvelles permanences.</p>
			<p>En 1974, le comité de la section décide d’élever un monument à la mémoire de toutes les victimes tombées au champ d’honneur du travail et avec la municipalité de Seclin, ce monument devait être inauguré lors du congrès départemental des mutilés du travail de Seclin le 13 juin 1975 à côté du monument aux morts.</p>
			<p>Il faut rappeler qu’il s’agit du seul monument en France construit par des militants et rappelant l’allégorie de l’aveugle et du paralytique.</p>
			<p>C’est en décembre 2003 que le conseil d’administration, à l’unanimité, décide de démissionner de la Fédération des mutilés du travail et de constituer sa propre association.</p>
			<p>C’est ainsi que lors de l’assemblée générale extraordinaire du 7 février 2004, à l’unanimité des adhérents présents, qu’est fondée l’A.A.D.V.A.H du NORD qui regroupe aujourd’hui plus de 900 adhérents qui tient à rappeler avec force qu’elle regroupe dans son sein, les accidents du travail, de la route, de la vie privée, des maladies professionnelles, des malades et invalides, des veufs et veuves, des handicapés.</p>
			<p>Elle estime que ce serait tromper, abuser odieusement ses adhérents que de les appeler à se grouper, à s’organiser, à s’organiser dans son sein pour une action concernant exclusivement leurs propres revendications pour les entraîner ensuite insidieusement, sous une forme plus ou moins habile à une action, à un mouvement qui peut être contraire à leurs propres sentiments, à leurs idées.</p>
			<p>Que son but n’est pas l’examen des questions politiques, économiques ou autres, sous toutes les formes, dans toute leur étendue et sous tous leurs aspects, mais uniquement l’amélioration de la situation des victimes du travail, de leurs ayants droit, des invalides et des handicapés, un point c’est tout.</p>
			<p>La cause des victimes du travail, tous régimes confondus, des assurés sociaux, des invalides, des handicapés est une cause que l’on doit servir et dont on ne doit pas chercher à se servir dans quelque but que ce soit. Elle est une cause de justice, d’équité et d’humanité qui doit rallier à elle, tous les travailleurs, qu’ils soient syndiqués et qu’ils appartiennent à quelque tendance ou inorganisés.</p>
			<p>Cela par solidarité envers leurs camarades, blessés à leurs côtés au cours de leur travail et par intérêt pour eux, étant appelé à chaque instant à être à leur tour victime d’un accident ou atteint d’une invalidité.</p>
			<p>Tous les gens de cœur, peu importe leur milieu social, leurs opinions politique, philosophique, religieuse à laquelle ils appartiennent et que ne peut laisser indifférent le sort de ceux qui sont tombés et tombent chaque jour sur ce que l’on a si justement appelé « le champ d’honneur du travail », de ce travail qui seul assure la vie de la collectivité toute entière et qui seul crée les richesses d’un pays.</p>
			<p>Venez renforcer la puissance numérique de notre association.</p>
			<p>Aux camarades, anciens adhérents, comme nous les membres du conseil d’administration, rappelle- toi que notre adhésion, notre présence, sont l’unique garant valable dont a besoin notre association pour continuer et faire valoir avec succès une politique sociale des handicapés.</p>
			<p>Si toi, ami(e) qui lis ces mots, tu n’as pas continué à adhérer à notre association pour des raisons personnelles que nous voulons respecter et qui sont peut être compréhensibles, pense tout de même que ton départ est toujours regretté mais que ton retour est toujours espéré.</p>
			<p>Rappelle-toi que rien n’est jamais acquis, tout peut être remis en cause, plus nous serons unis, plus fort nous serons.</p>
