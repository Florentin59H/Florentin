			<h1 class="nosactionsh1">Accident de la route</h1>
					
			<p class="nosactionsp">La loi du 5 juillet 1985, dite loi Badinter, a confié à l’assureur du responsable d’un accident de la route la mise en œuvre d’un examen médical amiable, forme la plus répandue actuellement d’expertise d’évaluation du dommage corporel.</p>

			<p class="nosactionsp">Cette organisation a manifestement accéléré le processus indemnitaire, mais n’a sans doute pas résolu l’ensemble des problèmes, puisque environ 15 % encore des dommages sont soumis à l’appréciation des juridictions.</p>

			<p class="nosactionsp">L’évaluation médico-légale du handicap et l’expertise sur laquelle elle se fonde soulèvent des questions communes à l’ensemble des expertises médicales, alors que d’autres sujets sont plus spécifiques.</p>

			<ul class="nosactionsul">
			
			
			<div id="nosactions-accident-a">
			
				<li class="nosactionslis"><h3 class="nosactionsh3">A) Accélération du règlement des sinistres :</h3>

					<p class="nosactionsp">La victime doit faire sa déclaration dans les 5 jours de l’accident à la suite d’un constat amiable ou de procès-verbaux de police, des témoins de l’accident sont souvent extrêmement utiles.</p>

				</li>
			
			</div>
			
			<div id="nosactions-accident-b">
				
				<l1 class="nosactionsli"><h3 class="nosactionsh3">B) Obligations de l’assureur :</h3>

					<ul class="nosactionsuls">

						<li class="nosactionlis"><h4 class="nosactionsh4">Communication des procès-verbaux de police :</h4>

							<ul class="nosactionsul">
								<li class="nosactionsli">L’assureur doit communiquer dans un délai de 4 mois les procès-verbaux de police qu’il a obtenus par l’intermédiaire de TRANS PV.</li>
							</ul>
							
						</li>

						<li class="nosactionslis">L’assureur est tenu, dès sa 1ère correspondance, d’informer la victime qu’elle peut obtenir la copie des procès-verbaux par son intermédiaire, qu’elle a la possibilité de se faire assister d’un avocat et d’un médecin de son choix.</li>
						
						<li class="nosactionslis"><h4 class="nosactionsh4">Offre d’indemnisation :</h4>
						
							<ul class="nosactionul">
								<li class="nosactionsli">l’assureur sollicite un certain nombre de renseignements de la victime afin d’établir une offre d’indemnisation - délai maximal de 8 mois : La loi impose à l’assureur ce délai à compter de l’accident pour transmettre son offre d’indemnisation à la victime, ou en cas de décès à ses ayants droits (si la victime est décédée plus d’un mois après l’accident, le délai est augmenté d’une durée supplémentaire équivalente à celle écoulée entre le premier mois et le décès). - examen médical de la victime : L’assureur peut organiser un examen médical de la victime, durant le délai de 8 mois. Si la victime récuse le médecin choisi par l’assureur, elle pourra demander la désignation d’un médecin à titre d’Expert par le juge des référés.</li>
								<li class="nosactionsli">Dans le délai de 20 jours à compter de l’examen médical, le médecin adresse un exemplaire de son rapport à l’assureur, à la victime et au médecin qui l’a assistée.</li>
							</ul>

						</li>
			
						<li class="nosactionslis"><h4 class="nosactionsh4">Allocation de provisions :</h4>

							
							<p class="nosactionsp">L’offre a un caractère provisionnel si la victime n’est pas consolidée.</p>

							<p class="nosactionsp">Dès que l’assureur est informé de la consolidation, il dispose d’un délai de 5 mois pour formuler une offre définitive.</p>

							<p class="nosactionsp">S’il ne transmet pas l’offre dans le délai imparti par la loi, une pénalité du double du taux de l’intérêt légal à compter de l’expiration du délai jusqu’au jour du jugement définitif pourra lui être imputé, et ce également en cas d’une offre insuffisante.</p>

							<h5 class="nosactionsh5">La victime peut refuser ou accepter l’offre :</h2>
								<ul class="nosactionsul">
									<li class="nosactionsli">Si elle refuse elle a intérêt à introduire une procédure judiciaire au plus vite.</li>
									<li class="nosactionsli">Si elle accepte, l’assureur doit lui transmettre un procès-verbal de transaction (la victime a 15 jours pour dénoncer la transaction).</li>
								</ul>

							<p class="nosactionsp">L’assureur aura des intérêts légaux à payer majorés de 50 % si le paiement n’intervient pas entre 1 mois et 3 mois après la transaction ou 2 mois après un jugement.</p>

							<p class="nosactionsp">Les victimes mineures ou majeures en tutelle : l’assureur doit informer le Juge des Tutelles avant tout paiement au représentant de la personne protégée.</p>

							<h5 class="nosactionsh5">Clauses d’exclusion de garantie d’assurance :</h5>
								<ul class="nosactionsul">
									<li class="nosactionsli">Conduite du véhicule par un non titulaire du permis</li>
									<li class="nosactionsli">Conduite en état d’ivresse</li>
								</ul>

							<p class="nosactionsp">Ces clauses sont inopposables à la victime, mais l’assureur peut se retourner contre le conducteur ou le propriétaire du véhicule pour obtenir le remboursement des sommes qu’il a payées.</p>

					</ul>
				</li>
				
